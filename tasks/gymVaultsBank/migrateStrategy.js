module.exports = async function ({ pid, newStrategy, caller }, { ethers: { getNamedSigners, getContract } }) {
	const signers = await getNamedSigners();

	const gymVaultsBank = await getContract("GymVaultsBank", signers[caller]);

	const tx = await gymVaultsBank.connect(signers[caller]).migrateStrategy(pid, newStrategy);

	return { tx };
};
