module.exports = async function ({ withdrawFee, caller }, { ethers: { getNamedSigners, getContract } }) {
	const signers = await getNamedSigners();

	const gymVaultsBank = await getContract("GymVaultsBank", signers[caller]);

	const tx = await gymVaultsBank.connect(signers[caller]).setWithdrawFee(withdrawFee);

	return { tx };
};
